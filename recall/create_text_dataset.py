def load_text_dataset(file_path=None):
    """
    加载文本数据集 - 自动查找datasets文件夹
    
    Args:
        file_path: 文件路径，如果为None则使用默认路径
    
    Returns:
        list: 文本列表
    """
    if file_path is None:
        file_path = os.path.join('datasets', 'rich_text_dataset.json')
    
    if not os.path.exists(file_path):
        raise FileNotFoundError(f"文件不存在: {file_path}")
    
    with open(file_path, 'r', encoding='utf-8') as f:
        dataset = json.load(f)
    
    # 提取文本列表
    texts = [item['text'] for item in dataset['data']]
    
    print(f"📖 从 {file_path} 加载了 {len(texts)} 条文本")
    print(f"📋 数据集版本: {dataset['metadata']['version']}")
    print(f"📅 创建日期: {dataset['metadata']['created_date']}")
    
    return texts

